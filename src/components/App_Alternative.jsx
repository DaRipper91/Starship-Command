// ─────────────────────────────────────────────────────────────────

function ColorSection({ colors, setColors }) {
  const contrast = (bg, fg) => {
    const hex = (h) => parseInt(h.replace('#', ''), 16);
    const lum = (c) => {
      const r = (hex(c) >> 16) & 0xff;
      const g = (hex(c) >> 8) & 0xff;
      const b = hex(c) & 0xff;
      const toL = (x) => {
        const s = x / 255;
        return s <= 0.03928 ? s / 12.92 : ((s + 0.055) / 1.055) ** 2.4;
      };
      return 0.2126 * toL(r) + 0.7152 * toL(g) + 0.0722 * toL(b);
    };
    try {
      const l1 = lum(bg);
      const l2 = lum(fg);
      const ratio = (Math.max(l1, l2) + 0.05) / (Math.min(l1, l2) + 0.05);
      return ratio.toFixed(1);
    } catch {
      return '?';
    }
  };

  const fields = [
    { key: 'bg', label: 'Background' },
    { key: 'fg', label: 'Foreground' },
    { key: 'accent', label: 'Accent (Primary)' },
    { key: 'accent2', label: 'Accent (Secondary)' },
  ];

  const contrastRatio = contrast(colors.bg, colors.fg);
  const contrastOk = parseFloat(contrastRatio) >= 4.5;

  return (
    <div>
      <div
        style={{
          display: 'grid',
          gridTemplateColumns: '1fr 1fr',
          gap: 10,
          marginBottom: 12,
        }}
      >
        {fields.map(({ key, label }) => (
          <div key={key}>
            <label
              style={{
                color: '#94a3b8',
                fontSize: 10,
                fontFamily: 'monospace',
                display: 'block',
                marginBottom: 4,
              }}
            >
              {label}
            </label>
            <div style={{ display: 'flex', gap: 6, alignItems: 'center' }}>
              <input
                type="color"
                value={colors[key] || '#888888'}
                onChange={(e) =>
                  setColors((c) => ({ ...c, [key]: e.target.value }))
                }
                style={{
                  width: 36,
                  height: 32,
                  border: 'none',
                  background: 'none',
                  cursor: 'pointer',
                  padding: 0,
                }}
              />
              <input
                value={colors[key] || ''}
                onChange={(e) =>
                  setColors((c) => ({ ...c, [key]: e.target.value }))
                }
                style={{
                  flex: 1,
                  background: 'rgba(255,255,255,0.05)',
                  border: '1px solid rgba(255,255,255,0.12)',
                  borderRadius: 6,
                  padding: '5px 8px',
                  color: '#e2e8f0',
                  fontSize: 11,
                  fontFamily: 'monospace',
                }}
              />
            </div>
          </div>
        ))}
      </div>

      {/* Contrast checker */}
      <div
        style={{
          background: contrastOk
            ? 'rgba(34,197,94,0.1)'
            : 'rgba(239,68,68,0.1)',
          border: `1px solid ${contrastOk ? '#22c55e44' : '#ef444444'}`,
          borderRadius: 7,
          padding: '8px 12px',
          fontSize: 11,
          fontFamily: 'monospace',
          color: contrastOk ? '#86efac' : '#fca5a5',
        }}
      >
        {contrastOk ? '✓' : '⚠'} Contrast ratio: {contrastRatio}:1 —{' '}
        {contrastOk ? 'WCAG AA Pass' : 'WCAG AA Fail (needs 4.5:1+)'}
      </div>
    </div>
  );
}

// ─────────────────────────────────────────────────────────────────
// HISTORY
// ─────────────────────────────────────────────────────────────────

function useHistory(initial) {
  const [history, setHistory] = useState([initial]);
  const [idx, setIdx] = useState(0);

  const current = history[idx];

  const push = useCallback(
    (newState) => {
      setHistory((h) => {
        const trimmed = h.slice(0, idx + 1);
        return [...trimmed, newState];
      });
      setIdx((i) => i + 1);
    },
    [idx],
  );

  const undo = useCallback(() => setIdx((i) => Math.max(0, i - 1)), []);
  const redo = useCallback(
    () => setIdx((i) => Math.min(history.length - 1, i + 1)),
    [history.length],
  );
  const canUndo = idx > 0;
  const canRedo = idx < history.length - 1;

  return { current, push, undo, redo, canUndo, canRedo };
}

