function generateTOML(config) {
  const {
    leftModules,
    rightModules,
    colors,
    separator,
    promptStyle,
    rightPromptEnabled,
  } = config;
  const leftOrder = leftModules.map((m) => m.id);
  const rightOrder = rightModules.map((m) => m.id);

  let toml = `# Generated by Starship Command Enhanced\n`;
  toml += `# https://starship.rs\n\n`;
  toml += `add_newline = ${promptStyle === 'multiline'}\n`;
  toml += `\n[palette]\n`;
  toml += `accent = "${colors.accent}"\n`;
  toml += `accent2 = "${colors.accent2}"\n\n`;

  toml += `format = """\n`;
  leftOrder.forEach((mod) => {
    toml += `[${mod}]\\\n`;
  });
  toml += `$character\n"""\n\n`;

  if (rightPromptEnabled && rightOrder.length > 0) {
    toml += `right_format = """\n`;
    rightOrder.forEach((mod) => {
      toml += `[${mod}]\\\n`;
    });
    toml += `\n"""\n\n`;
  }

  toml += `[character]\n`;
  toml += `success_symbol = "[${config.promptChar || '❯'}](bold green)"\n`;
  toml += `error_symbol = "[${config.promptChar || '❯'}](bold red)"\n\n`;

  toml += `[directory]\n`;
  toml += `style = "bold ${colors.accent}"\n`;
  toml += `truncation_length = 3\n\n`;

  toml += `[git_branch]\n`;
  toml += `symbol = " "\n`;
  toml += `style = "bold ${colors.accent2}"\n\n`;

  toml += `[git_status]\n`;
  toml += `style = "bold red"\n\n`;

  if (separator !== 'None') {
    const sep = SEPARATOR_SHAPES[separator];
    toml += `# Separator: ${sep?.label}\n`;
    toml += `# Left separators: ${sep?.left?.join(' ')}\n`;
    toml += `# Right separators: ${sep?.right?.join(' ')}\n\n`;
  }

  return toml;
}

