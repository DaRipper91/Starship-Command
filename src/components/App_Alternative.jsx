function TOMLPanel({ config }) {
  const [copied, setCopied] = useState(false);
  const toml = generateTOML(config);

  const copy = () => {
    navigator.clipboard.writeText(toml).then(() => {
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    });
  };

  const download = () => {
    const blob = new Blob([toml], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'starship.toml';
    a.click();
    URL.revokeObjectURL(url);
  };

  const shareUrl = () => {
    const encoded = btoa(JSON.stringify(config));
    const url = `${window.location.origin}${window.location.pathname}#theme=${encoded}`;
    navigator.clipboard.writeText(url);
    alert('Shareable URL copied to clipboard!');
  };

  return (
    <div>
      <div style={{ display: 'flex', gap: 8, marginBottom: 10 }}>
        <button onClick={copy} style={btnStyle(copied ? '#22c55e' : '#8b5cf6')}>
          {copied ? '✓ Copied!' : ' Copy TOML'}
        </button>
        <button onClick={download} style={btnStyle('#06b6d4')}>
          ⬇ Download starship.toml
        </button>
        <button onClick={shareUrl} style={btnStyle('#f59e0b')}>
          🔗 Share URL
        </button>
      </div>
      <pre
        style={{
          background: 'rgba(0,0,0,0.4)',
          border: '1px solid rgba(255,255,255,0.08)',
          borderRadius: 8,
          padding: 14,
          fontSize: 11,
          fontFamily: "'JetBrains Mono', monospace",
          color: '#94a3b8',
          maxHeight: 320,
          overflowY: 'auto',
          whiteSpace: 'pre-wrap',
        }}
      >
        {toml}
      </pre>
    </div>
  );
}

